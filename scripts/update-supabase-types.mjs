import { execSync } from "node:child_process";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

function getEnvValue(key) {
  if (process.env[key]) {
    return process.env[key];
  }

  const envPath = resolve(".env.local");
  if (!existsSync(envPath)) {
    return undefined;
  }

  const file = readFileSync(envPath, "utf-8");
  for (const rawLine of file.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) {
      continue;
    }
    const separatorIndex = line.indexOf("=");
    if (separatorIndex <= 0) {
      continue;
    }
    const envKey = line.slice(0, separatorIndex).trim();
    if (envKey !== key) {
      continue;
    }
    const value = line.slice(separatorIndex + 1).trim();
    return value.replace(/^['"]|['"]$/g, "");
  }

  return undefined;
}

const outputPath = resolve("lib", "supabase.types.ts");
const projectRefFromEnv = getEnvValue("SUPABASE_PROJECT_REF");
const supabaseUrl = getEnvValue("NEXT_PUBLIC_SUPABASE_URL");
let projectId = projectRefFromEnv;
if (!projectId && supabaseUrl) {
  try {
    const hostname = new URL(supabaseUrl).hostname;
    const match = hostname.match(/^([a-z0-9-]+)\.supabase\.co$/i);
    if (match) {
      projectId = match[1];
    }
  } catch {
    // Ignore invalid URL and keep validation below.
  }
}
const schema = getEnvValue("SUPABASE_SCHEMA") ?? "public";
const accessToken = getEnvValue("SUPABASE_ACCESS_TOKEN");

if (!projectId) {
  console.error(
    "Missing SUPABASE_PROJECT_REF and could not derive project ref from NEXT_PUBLIC_SUPABASE_URL.",
  );
  process.exit(1);
}

let generatedTypes = "";

try {
  generatedTypes = execSync(
    `npx supabase gen types typescript --project-id ${projectId} --schema ${schema}`,
    {
      encoding: "utf-8",
      stdio: ["ignore", "pipe", "pipe"],
      env: {
        ...process.env,
        ...(accessToken ? { SUPABASE_ACCESS_TOKEN: accessToken } : {}),
      },
    },
  );
} catch (error) {
  console.error("Failed to generate Supabase types.");
  if (error?.stderr) {
    console.error(String(error.stderr));
  } else if (error instanceof Error) {
    console.error(error.message);
  } else {
    console.error(String(error));
  }
  process.exit(1);
}

mkdirSync(dirname(outputPath), { recursive: true });

const header = [
  "// Auto-generated by `npm run supabase:types`.",
  "// Do not edit manually.",
  "",
].join("\n");

writeFileSync(outputPath, `${header}${generatedTypes.trim()}\n`, "utf-8");

console.log(`Updated ${outputPath}`);
